package Lezione8;

public class Treni {
	int km;
	String città;
	public Treni(int km, String città) {
		this.km = km;
		this.città = città;
	}
	public int getKm() {
		return km;
	}
	public void setKm(int km) {
		this.km = km;
	}
	public String getCittà() {
		return città;
	}
	public void setCittà(String città) {
		this.città = città;
	}
	
}
